//Write a program to check two integers whether are equal or not.

#include<stdio.h>
int main(){
     int num1, num2;
     printf("input the two integer numbers ::");
     scanf("%d %d",&num1,&num2);
     if(num1==num2){
        printf("two integers are equal.");
     }
     else{
          printf("two integers are not equal.");
     }

return 0;
}
